public class HW6_2 {
//        Задача №1
//        Необходимо написать 4 метода:
//        сложение 2х чисел
//        вычитание 2х чисел
//        умножение 2х чисел
//        деление 2х чисел
    public static double getSum(double a, double b) {
        return a + b;
    }

    public static double getMinus(double a, double b) {
        return a - b;
    }

    public static double getDev(double a, double b) {
        return a / b;
    }

    public static double getMulti(double a, double b) {
        return a + b;
    }

    public static void main(String[] args) {

        System.out.println(getSum(20,80));
        System.out.println(getMinus(20,80));
        System.out.println(getDev(20,80));
        System.out.println(getMulti(20,80));
    }
}
